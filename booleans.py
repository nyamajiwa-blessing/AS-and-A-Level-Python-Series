# declaring and assigning booleans
is_registered = True
available = False
print(is_registered)
print(available)

num1 = 100
num2 = 200
# relational operators return boolean values
# greater
print(num1 > num2)

# lesser
print(num1 < num2)

# equal
print(num1 == num2)

# not equal
print(num1 != num2)

# greater-equal
print(num1 >= num2)

# lesser-equal
print(num1 <= num2)